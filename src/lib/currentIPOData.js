// Get IPO statistics
export const getCurrentIPOStats = () => {
  const ipos = getCurrentIPOData()
  
  return {
    totalIPOs: ipos.length,
    openIPOs: ipos.filter(ipo => ipo.status === 'Open').length,
    upcomingIPOs: ipos.filter(ipo => ipo.status === 'Upcoming').length,
    mainBoardIPOs: ipos.filter(ipo => ipo.boardType === 'Main Board').length,
    smeIPOs: ipos.filter(ipo => ipo.boardType === 'SME').length,
    profitableIPOs: ipos.filter(ipo => ipo.isProfitable).length,
    avgGMP: parseFloat((ipos.reduce((sum, ipo) => sum + ipo.gmp, 0) / ipos.length).toFixed(2)),
    totalIssueSize: ipos.reduce((sum, ipo) => sum + ipo.issueSize, 0),
    lastUpdated: new Date().toISOString()
  }
}